package no.ntnu.fp.su;

/**
 * LACinfo er et objekt som inneholder id til LAC, i tillegg til
 * tilkoblingsadresse og portnummer. Denne informasjonen vil bli brukt
 * av
 * 
 * @author askeland
 *
 */

public class LACinfo {
	private String id, remoteAddress;
	private int remotePort;
	
	public LACinfo(String id, String remoteAddress, int remotePort){
		this.id = id;
		this.remoteAddress = remoteAddress;
		this.remotePort = remotePort;
	}
	
	public String getId(){
		return id;
	}
	
	public String getRemoteAddress(){
		return remoteAddress;
	}
	
	public int getRemotePort() {
		return remotePort;
	}
	
	public void setRemoteAddress(String newAddress) {
		remoteAddress = newAddress;
	}
	
	public void setRemotePort(int newPort) {
		remotePort = newPort;
	}
}
